    private void prosesLogin() {
        if(validasiInput() == true) {
            String id = t_user.getText();
            String user = t_user.getText();
            String pass = jp_pass.getText();
            
            model_login mod_log = new model_login();
            mod_log.setId_user(id);
            mod_log.setUser_name(user);
            mod_log.setPass_user(pass);
           
            login.prosesLogin(mod_log);
            
            if(close) {
                dispose();
            }
        }
    }
    
    
    private boolean validasiInput() {
        boolean valid = false;
        if(t_user.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
        } else if(jp_pass.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        } else {
            valid = true;
        }
        
        return valid;
    }
