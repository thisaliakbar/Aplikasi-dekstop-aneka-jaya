    private boolean validasiInput() {
        boolean valid = false;
        if(t_idSup.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "ID Suppler tidak boleh kosong");
        } else if(t_namaSup.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama Supplier tidak boleh kosong");
        } else if(t_noTelp.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No telephone tidak boleh kosong");
        } else if(t_alamat.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong");
        } else {
            valid = true;
        
        }
        
        return valid;
    }
    
