    private boolean validasiInput() {
        boolean valid = false;
        if(t_nama.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong");
        } else if(t_username.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
        } else if(j_pass.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
        } else if(t_telp.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No Telephone tidak boleh kosong");
        } else if(t_alamat.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong");
        } else if(cbxLevel.getSelectedItem().equals("Level")) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih level");
        }
        else {
            valid = true;
        }
        
        return valid;
    }
    
