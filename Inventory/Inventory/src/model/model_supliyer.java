
package model;

public class model_supliyer {
    private String id_supliyer;
    private String nama_supliyer;
    private String telp_supliyer;
    private String alamat_supliyer;

    public String getId_supliyer() {
        return id_supliyer;
    }

    public void setId_supliyer(String id_supliyer) {
        this.id_supliyer = id_supliyer;
    }

    public String getNama_supliyer() {
        return nama_supliyer;
    }

    public void setNama_supliyer(String nama_supliyer) {
        this.nama_supliyer = nama_supliyer;
    }

    public String getTelp_supliyer() {
        return telp_supliyer;
    }

    public void setTelp_supliyer(String telp_supliyer) {
        this.telp_supliyer = telp_supliyer;
    }

    public String getAlamat_supliyer() {
        return alamat_supliyer;
    }

    public void setAlamat_supliyer(String alamat_supliyer) {
        this.alamat_supliyer = alamat_supliyer;
    }

}
