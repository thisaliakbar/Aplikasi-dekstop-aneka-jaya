    private void pencarian() {
        List<model_barang> list = service.pencarianData(t_cari.getText());
        tblModel.setData(list);
    }
    
    private void loadData() {
        List<model_barang> list = service.getData();
        tblModel.setData(list);
    }
    
    private void pilihData() {
        int row = tbl_Barang.getSelectedRow();
        model_barang brg = new model_barang();
        brg.setStok(Integer.parseInt(tbl_Barang.getModel().getValueAt(row, 6).toString()));
        
        if(service.validasiStokKosong(brg) == true) {
        Barang.setKode_barang(tbl_Barang.getModel().getValueAt(row, 0).toString());
        Barang.setNama_barang(tbl_Barang.getModel().getValueAt(row, 3).toString());
        Barang.setSatuan(tbl_Barang.getModel().getValueAt(row, 4).toString());
        Barang.setHarga(Long.parseLong(tbl_Barang.getModel().getValueAt(row,5).toString()));
        Barang.setStok(Integer.parseInt(tbl_Barang.getModel().getValueAt(row, 6).toString()));
        dispose();
        }  
    }
    
    static public class HeaderColor extends DefaultTableCellRenderer{

        public HeaderColor() {
            setOpaque(true);
        }
        
        public Component getTableCellRendererComponent(JTable tbl_contoh, Object value, boolean selected, boolean focused, int row, int column) {
            super.getTableCellRendererComponent(tbl_contoh, value, selected, focused, row, column);
            setBackground(new Color(0,139,185,255));
            setForeground(new Color(255, 255, 255));
            setFont(new Font(null, Font.BOLD, 14));
            return this;
        }
    
    }
    
