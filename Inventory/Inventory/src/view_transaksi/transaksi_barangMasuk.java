    private void bt_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_submitActionPerformed
        if(validasiInput()== true) {
        list = serviceDetPem.getData(txt_noPesan.getText());
        tblModelDetPem.setData(list);
        
        model_detail_pemesanan mdp = new model_detail_pemesanan();
        model_pemesanan mp = new model_pemesanan();
        
        String no_pesan = txt_noPesan.getText();
        Long subtotal = mdp.getSubtotal_pesan();
        
        mp.setNo_pesan(no_pesan);
        mdp.setMod_pemesanan_detPem(mp);
        mdp.setSubtotal_pesan(subtotal);
        serviceDetPem.sumTotalDetail(mdp);
        txt_totalMas.setText(mdp.getSubtotal_pesan().toString());  
        }
        
        
    }//GEN-LAST:event_bt_submitActionPerformed

