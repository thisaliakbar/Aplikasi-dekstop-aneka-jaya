    private boolean validasiInput() {
        boolean valid = false;
        if(txt_noPesan.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No Pesan tidak boleh kosong");
        } else {
            valid = true;
        }
        return valid;
    }
    
