    private boolean validasiInput() {
        boolean valid = false;
        if(t_kodeBarang.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode Barang tidak boleh kosong");
        } 
        else if(cbx_satuan.getSelectedItem().equals("Pilih Satuan")) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih satuan");
        } else if(t_jumlah.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Jumlah tidak boleh kosong");
        } else if(cbx_keterangan.getSelectedItem().equals(null)) {
            JOptionPane.showMessageDialog(null, "Keterangan tidak boleh kosong");
        }
        
        else {
            valid = true;
        }
        return valid;
    }
    
