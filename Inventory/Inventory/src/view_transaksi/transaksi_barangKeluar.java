    private void tambahDataSementara() {
        if(validasiInput() == true) {
        Control_barang serviceBar = new Crud_Barang();
        model_barang brg = new model_barang();
        brg.setStok(Integer.parseInt(t_qty.getText()));
        
        int qty = Integer.parseInt(t_jumlah.getText());
   
        if(serviceBar.validasiQtyStok(brg, qty)) {
            
        String kodeKel = t_kodeBarang.getText();
        String namaKel = t_namaBarang.getText();
        String satuanKel = cbx_satuan.getSelectedItem().toString();
        Long hargaKel = Long.parseLong(t_harga.getText());
        Integer jumlahKel = Integer.parseInt(t_jumlah.getText());
        Long subtotal_Kel = Long.parseLong(t_subTotal.getText());
        String ketKel = cbx_keterangan.getSelectedItem().toString();
        
        model_barang mb = new model_barang();
        model_detail_barangKeluar mdbk = new model_detail_barangKeluar();
        sementara_keluar sk = new sementara_keluar();
        
        mb.setKode_barang(kodeKel);
        mb.setNama_barang(namaKel);
        mb.setSatuan(satuanKel);
        mb.setHarga(hargaKel);
        
        mdbk.setJml_keluar(jumlahKel);
        mdbk.setSubtotal_keluar(subtotal_Kel);
        Long subKel = mdbk.getSubtotal_keluar();
        mdbk.setSubtotal_keluar(subKel);
        mdbk.setKeterangan(ketKel);
        
        sk.setMdl_brg(mb);
        sk.setMdl_mdbk(mdbk);
        
        serviceSem.tambahData(sk);
        serviceDet.sumTotal(mdbk);
        t_totalKel.setText(mdbk.getSubtotal_keluar().toString());
        if(JOptionPane.showConfirmDialog(this, "Mau Tambah barang?", "Konfirmasi", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
        bersihBarang();
        } else {
        bersihBarang();
        }
        
        loadDataSem();
        
        }
        }
        
    }
    
